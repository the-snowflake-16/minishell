#include "minishell.h"

void	sigint_handler(int signo)
{
	(void)signo;
	write(1, "\n", 1);
	rl_on_new_line();
	rl_replace_line("", 0);
	rl_redisplay();
}

void	update_exit_code(t_state *s, int status)
{
	if (WIFEXITED(status))
		s->last_exit_code = WEXITSTATUS(status);
}
